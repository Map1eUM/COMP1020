package HugeGraphicsExample.graphicsV3;

/* A very simple class that encapsulates an (x,y) pair
  * of coordinates as a single object, for convenience.
  * Everything is public. There is only a constructor.
  */
public class Point {
    public double x;
    public double y;
    public Point(double initX, double initY){
       x = initX;
       y=initY;
    }//constructor
 }//Point class
